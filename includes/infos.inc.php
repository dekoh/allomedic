
<h1 class="titreinfo">Allomedic c'est quoi?</h1>
<div class="blo">
	<div class="col pr-5">
		<h2>Gestion des rendez-vous</h2>
		<p>La gestion des rendez-vous vous permet d'ajouter des rendez-vous et de les visualiser dans un agenda personnalisable.</p>
		<p> Vous pouvez visualiser votre planning de la semaine ou bien de la journée. Sur la vue de la semaine vous pouvez facilement voir quand il y a des plages libres et y rajouter un rendez-vous facilement.</p>
		<p> Il est possible également de modifier vos disponibilités, vos horaires de travail, le temps des rendez-vous et aussi le type de rendez-vous par exemple si c'est à domicile ou au cabinet.</p>
	</div>
