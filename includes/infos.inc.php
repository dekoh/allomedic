</div>
<div class="blo">
	<div id="mbp" class="col pr-5"></div>
	<div class="col pr-5">
		<h2>Prise de rendez-vous en ligne</h2>
		<p>Allomedic permet aux patients de prendre rendez-vous en ligne sans devoir appeler le médecin.</p>
		<p>Cela permet au patient de prendre rendez-vous quand il veut sans se soucier de l'heure mais aussi cela permet au médecin de ne pas perdre trop de temps au téléphone.</p>
	</div>
</div>
<div class="blo">
	<div class="col pr-5">
		<h2>Gestion des patients</h2>
		<p>La gestion des patients vous permet d'ajouter des patients et de les modifier.</p>
		<p>Via une adresse email vous pouvez lier un patient à un compte utilisateur sur la plateforme et ainsi permettre à ce patient de prendre rendez-vous. Si le patient n'a pas internet, vous pouvez quand même le rajouter et ainsi prendre rendez-vous sur la plateforme pour lui quand il appellera.</p>
